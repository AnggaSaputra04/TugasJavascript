//---------------hewan ternak-----------------
$("document").ready(function () {
    $("div.bebek").click(function () {
        $(this).css({
            background: "url('https://www.google.com/logos/fnbx/animal_sounds/duck.png')"
        });
    });
    $("div.bebek").click(function () {
        $("<audio></audio>").attr({
            'src': 'audio/suara-bebek.mp3',
            'volume': 0.4,
            'autoplay': 'autoplay'
        }).appendTo("div");
    });
});
